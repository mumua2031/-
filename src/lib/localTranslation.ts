const punctuationPairs: Array<[RegExp, string]> = [
  [/：/g, ': '],
  [/；/g, '; '],
  [/，/g, ', '],
  [/。/g, '. '],
  [/、/g, ', '],
  [/（/g, ' ('],
  [/）/g, ') '],
  [/“|”/g, '"'],
  [/《|》/g, '"'],
  [/＋/g, '+'],
];

function tidy(value: string) {
  return value
    .replace(/([a-z])([A-Z])/g, '$1 $2')
    .replace(/\b(to|as|or|and)(?=[a-zA-Z])/g, '$1 ')
    .replace(/\s+([,.;:)])/g, '$1')
    .replace(/([(])\s+/g, '$1')
    .replace(/\s{2,}/g, ' ')
    .replace(/\s+\./g, '.')
    .replace(/,\s*,/g, ',')
    .trim();
}

export function translateZhToEn(value: string, fallback = '') {
  let translated = value.trim();
  if (!translated) return fallback;

  for (const [source, target] of phrasePairs.sort((a, b) => b[0].length - a[0].length)) {
    translated = translated.split(source).join(target);
  }
  for (const [pattern, replacement] of punctuationPairs) {
    translated = translated.replace(pattern, replacement);
  }

  translated = translated.replace(cjkRunPattern, ' ');
  translated = tidy(translated);
  return translated || fallback;
}
